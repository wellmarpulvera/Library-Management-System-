    public void loadUser(int userId) {
        ResultSet rs = dbHelper.getUser(userId);
        try {
            if (rs != null && rs.next()) {
                int id = rs.getInt("user_id");
                String username = rs.getString("username");
                String password = rs.getString("password");
                String role = rs.getString("role");
                userIdLbl.setText(String.valueOf(id));
                usernameTxt.setText(username);
                passwordTxt.setText(password);
                if (role.equals("librarian")) {
                    librarianRb.setSelected(true);
                } else if (role.equals("user")) {
                    userRb.setSelected(true);
                }

                // Use the retrieved details as needed
            } else {
                // User not found or other handling
            }
        } catch (SQLException ex) {
            Logger.getLogger(UpdateUserFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

