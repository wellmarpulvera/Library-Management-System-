    private void updateReservationBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_updateReservationBtnMouseClicked
        // TODO add your handling code here:
        int userId = Integer.parseInt(userIdTxt.getText());
        int bookId = Integer.parseInt(bookIdTxt.getText());
        String reservationDate = reservationDateTxt.getText();
        SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
        try {
            dbHelper.updateReservation(Integer.parseInt(reservationIdLbl.getText()), userId, bookId, sdf.parse(reservationDate));
        } catch (ParseException ex) {
            Logger.getLogger(UpdateReservationFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        DashboardFrame dashboardFrame = new DashboardFrame();
        dashboardFrame.setVisible(true);
        dispose();
    }//GEN-LAST:event_updateReservationBtnMouseClicked

