    private void updateUserBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_updateUserBtnMouseClicked
        // TODO add your handling code here:
        String username = usernameTxt.getText();
        char[] passwordChars = passwordTxt.getPassword();
        String password = new String(passwordChars);
        String role = "";
        if (librarianRb.isSelected()) {
            role = "librarian";
        } else if (userRb.isSelected()) {
            role = "user";
        }
        dbHelper.updateUser(Integer.parseInt(userIdLbl.getText()), username, password, role);
        DashboardFrame dashboardFrame = new DashboardFrame();
        dashboardFrame.setVisible(true);
        dispose();
    }//GEN-LAST:event_updateUserBtnMouseClicked

