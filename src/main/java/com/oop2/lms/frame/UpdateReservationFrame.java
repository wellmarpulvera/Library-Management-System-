    public void loadReservation(int reservationId) {
        ResultSet rs = dbHelper.getReservation(reservationId);
        try {
            if (rs != null && rs.next()) {
                int id = rs.getInt("reservation_id");
                int userId = rs.getInt("user_id");
                int bookId = rs.getInt("book_id");
                Date reservationDate = rs.getDate("reservation_date");
                reservationIdLbl.setText(String.valueOf(id));
                userIdTxt.setText(String.valueOf(userId));
                bookIdTxt.setText(String.valueOf(bookId));
                SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
                reservationDateTxt.setText(sdf.format(reservationDate));
            }
        } catch (SQLException ex) {
            Logger.getLogger(UpdateUserFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

